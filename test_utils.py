from utils import get_points_in_triangle


def test_get_points_in_triangle():
    triangles = (((6, 1), (1, 2), (3, 4)),
                 ((5, 1), (2, 5), (8, 3)),
                 ((1, 1), (1, 5), (6, 1)),
                 ((2, 1), (2, 5), (7, 4)),
                 ((4, 1), (9, 3), (9, 0)),
                 ((1, 1), (6, 4), (8, 1)))
    points = ('set([(1, 2), (3, 2), (3, 3), (5, 2), (6, 1), (2, 3), (4, 3), (2, 2), (4, 2), (3, 4)])',
              'set([(7, 3), (8, 3), (5, 2), (4, 4), (6, 3), (6, 2), (4, 3), (5, 1), (2, 5), (3, 4), (5, 3)])',
              'set([(1, 2), (3, 2), (1, 3), (3, 3), (6, 1), (3, 1), (2, 1), (2, 4), (1, 5), (2, 3), (1, 4), (2, 2), (5, 1), (4, 2), (4, 1), (1, 1)])',
              'set([(6, 4), (3, 2), (3, 3), (4, 4), (5, 4), (2, 1), (7, 4), (2, 3), (4, 3), (2, 2), (2, 5), (3, 4), (2, 4), (5, 3)])',
              'set([(9, 0), (8, 2), (7, 1), (8, 1), (6, 1), (9, 3), (9, 2), (5, 1), (4, 1), (7, 2), (9, 1)])',
              'set([(6, 4), (3, 2), (7, 1), (8, 1), (5, 2), (6, 1), (3, 1), (2, 1), (6, 3), (6, 2), (5, 1), (4, 2), (4, 1), (1, 1), (7, 2), (5, 3)])')

    for triangle, p in zip(triangles, points):
        assert get_points_in_triangle(triangle) == eval(p)
